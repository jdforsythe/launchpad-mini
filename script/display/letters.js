import { getLedMap } from './display.js';

export async function displayLetter(launchPadOutput, delay, letter, colors) {
  return new Promise((resolve) => {
    setTimeout(
      () => {
        launchPadOutput.reset(0);

        const ledMap = getLedMap(letter, colors);

        launchPadOutput.setDisplay(ledMap);

        return resolve();
      },
      delay,
    );
  })
}

